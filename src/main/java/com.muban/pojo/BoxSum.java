package com.muban.pojo;

public class BoxSum {
    int LsizeQty;
    int MsizeQty;
    int SsizeQty;
    int BoxSum;
    int QtySum;
    int LSize;
    int MSize;
    int SSize;
    int LastQty;

    public int getLsizeQty() {
        return LsizeQty;
    }

    public void setLsizeQty(int lsizeQty) {
        LsizeQty = lsizeQty;
    }

    public int getMsizeQty() {
        return MsizeQty;
    }

    public void setMsizeQty(int msizeQty) {
        MsizeQty = msizeQty;
    }

    public int getSsizeQty() {
        return SsizeQty;
    }

    public void setSsizeQty(int ssizeQty) {
        SsizeQty = ssizeQty;
    }

    public int getBoxSum() {
        return BoxSum;
    }

    public void setBoxSum(int boxSum) {
        BoxSum = boxSum;
    }

    public int getQtySum() {
        return QtySum;
    }

    public void setQtySum(int qtySum) {
        QtySum = qtySum;
    }

    public int getLSize() {
        return LSize;
    }

    public void setLSize(int LSize) {
        this.LSize = LSize;
    }

    public int getMSize() {
        return MSize;
    }

    public void setMSize(int MSize) {
        this.MSize = MSize;
    }

    public int getSSize() {
        return SSize;
    }

    public void setSSize(int SSize) {
        this.SSize = SSize;
    }

    public int getLastQty() {
        return LastQty;
    }

    public void setLastQty(int lastQty) {
        LastQty = lastQty;
    }
}
